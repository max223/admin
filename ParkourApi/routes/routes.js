router.post('/actions/validateRoute',
  (req, res) => {
    let route_id = req.body.data.attributes.ids[0];

    return routes
      .update({
        status: 1
      }, {
        where: {
          id: route_id
        }
      })
      .then(() => {
        res.send({
          success: 'Route sent to be published !'
        });
      });
  });


